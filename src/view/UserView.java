    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
       
        String keyword = txtCari.getText().trim(); // Ambil keyword
        if (cbKategori.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Pilih kategori pencarian!");
            return;
        }
        String category = cbKategori.getSelectedItem().toString(); // Ambil kategori

        // 3. Panggil method search BARU dari controller
        // Kita menggunakan 'jTable1' sesuai nama variabel di initComponents() barumu
        dc.cariUntukUserView(this.jTable1, keyword, category); 
        setTombolReview();


    }//GEN-LAST:event_btnCariActionPerformed

