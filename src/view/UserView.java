    private void btnLogoutUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutUserActionPerformed
         int response = JOptionPane.showConfirmDialog(this, 
        "Apakah Anda yakin ingin logout?", 
        "Konfirmasi Logout", 
        JOptionPane.YES_NO_OPTION, 
        JOptionPane.QUESTION_MESSAGE);
    
    if (response == JOptionPane.YES_OPTION) {
        JOptionPane.showMessageDialog(this, "Anda berhasil logout.", "Logout Berhasil", JOptionPane.INFORMATION_MESSAGE);
        new LoginForm().setVisible(true); // Buka LoginForm
        this.dispose(); // Tutup UserView
    }
    }//GEN-LAST:event_btnLogoutUserActionPerformed
    
    
    
